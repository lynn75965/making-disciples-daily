// scripts/sync-constants.cjs
// Making Disciples Daily -- constant mirror sync
//
// Copies the authoritative frontend SSOT constant files from src/constants/
// into supabase/functions/_shared/ so Edge Functions import the SAME values the
// frontend uses (Architecture Principle #2: Frontend Drives Backend).
//
// The _shared/ copies are GENERATED. Never hand-edit them -- they are
// overwritten on the next sync. When you change a file in FILES_TO_SYNC, run:
//
//   npm run sync-constants
//
// (Wire this in package.json: "sync-constants": "node scripts/sync-constants.cjs")
//
// The mirror is written UTF-8 WITHOUT BOM so it passes the ASCII guard, with a
// generated-file banner prepended. A post-write check fails loudly if any
// non-ASCII byte or BOM slipped through.

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const SRC_DIR = path.join(ROOT, 'src', 'constants');
const DEST_DIR = path.join(ROOT, 'supabase', 'functions', '_shared');

// -------------------------------------------------------------------------
// FILES_TO_SYNC -- the frontend SSOT files that have a backend counterpart.
// Add a file here ONLY when an Edge Function needs to import it. Keep this list
// minimal; every entry is a maintenance obligation.
//
// v1 starts with the enum/contract and policy files the three v1 Edge Functions
// (invite email, reminder generator, GDPR export) actually read. Expand as
// backend needs grow. Files NOT listed here are frontend-only.
// -------------------------------------------------------------------------
const FILES_TO_SYNC = [
  'contracts.ts',
  'routes.ts',
  'accessControl.ts',
  'questionTypes.ts',
  'prayerVisibility.ts',
  'reminderPolicy.ts',
  'invitationConfig.ts',
  'bibleVersions.ts',
  'scriptureGuardrail.ts',
  'growthSigns.ts',
  'apprenticeStages.ts',
];

const BANNER = [
  '// AUTO-GENERATED by scripts/sync-constants.cjs -- DO NOT EDIT.',
  '// Source of truth: src/constants/<filename>.',
  '// Edit the frontend file and run: npm run sync-constants',
  '',
  '',
].join('\n');

function hasNonAscii(str) {
  for (let i = 0; i < str.length; i++) {
    if (str.charCodeAt(i) > 0x7f) return i;
  }
  return -1;
}

function main() {
  if (!fs.existsSync(DEST_DIR)) {
    fs.mkdirSync(DEST_DIR, { recursive: true });
  }

  let synced = 0;
  let missing = [];

  FILES_TO_SYNC.forEach(function (name) {
    const srcPath = path.join(SRC_DIR, name);
    const destPath = path.join(DEST_DIR, name);

    if (!fs.existsSync(srcPath)) {
      missing.push(name);
      return;
    }

    let content = fs.readFileSync(srcPath, 'utf8');

    // Strip a BOM if the source somehow has one.
    if (content.charCodeAt(0) === 0xfeff) {
      content = content.slice(1);
    }

    const bad = hasNonAscii(content);
    if (bad !== -1) {
      console.error(
        'SYNC ABORTED: non-ASCII character in source ' + name +
        ' at index ' + bad + '. Fix the frontend file first.'
      );
      process.exit(1);
    }

    const out = BANNER.replace('<filename>', name) + content;

    // Write UTF-8 without BOM.
    fs.writeFileSync(destPath, out, { encoding: 'utf8' });

    // Verify what landed on disk.
    const check = fs.readFileSync(destPath);
    if (check.length >= 3 &&
        check[0] === 0xef && check[1] === 0xbb && check[2] === 0xbf) {
      console.error('SYNC ERROR: BOM written to ' + name + '. Aborting.');
      process.exit(1);
    }
    for (let i = 0; i < check.length; i++) {
      if (check[i] > 0x7f) {
        console.error(
          'SYNC ERROR: non-ASCII byte in generated ' + name +
          ' at byte ' + i + '. Aborting.'
        );
        process.exit(1);
      }
    }

    synced++;
    console.log('  synced ' + name);
  });

  if (missing.length) {
    console.error(
      '\nSYNC INCOMPLETE: ' + missing.length + ' file(s) in FILES_TO_SYNC do ' +
      'not exist yet in src/constants/:'
    );
    missing.forEach(function (m) { console.error('  missing ' + m); });
    console.error(
      'Create the SSOT file or remove it from FILES_TO_SYNC. ' +
      '(During early Phase 0 some stubs may not exist yet.)'
    );
    process.exit(1);
  }

  console.log(
    '\nsync-constants clean: ' + synced + '/' + FILES_TO_SYNC.length +
    ' mirrored to supabase/functions/_shared/.'
  );
  process.exit(0);
}

main();
